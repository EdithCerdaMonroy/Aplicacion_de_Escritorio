    private void btnBuscarPredioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPredioActionPerformed
         try {
            conectarBase();
            objetoPredio.setClave(Float.parseFloat(txfClaveEditar.getText()));
            rs=stmt.executeQuery("SELECT * from tpredio where clave = '"+objetoPredio.getClave()+"'");
            JOptionPane.showMessageDialog(null,"Buscando el Predio... ");
            if (rs.next()==true) {
                //imprimir datos del libro encontrado
                this.txfSuperficieEditar.setText(rs.getString("superficie"));
                this.txfValorEditar.setText(rs.getString("valor"));
                this.txfUbicacionEditar.setText(rs.getString("ubicacion"));
                this.txfAnioEditar.setText(rs.getString("anio"));
                this.txfTotalEditar.setText(rs.getString("total"));
            } else {
                JOptionPane.showMessageDialog(null,"Error no exite el predio, valida que la clave sea correcta.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,"Error de BD busqueda"+ e);
        }
    }//GEN-LAST:event_btnBuscarPredioActionPerformed

