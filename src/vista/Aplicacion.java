    private void btnActualizarPredioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPredioActionPerformed
        try {
            conectarBase();
            //Objeto almacenamiento logico de datos, modelo de datos
            DefaultTableModel modeloDatos = new DefaultTableModel();
            jtConsultaPredios.setModel(modeloDatos);
            rs = stmt.executeQuery("select * from tpredio");

            ResultSetMetaData rsmd = rs.getMetaData();
            int cantidadColumnas = rsmd.getColumnCount();
            //titulos de las col:

            modeloDatos.addColumn("Id Predio");
            modeloDatos.addColumn("Clave");
            modeloDatos.addColumn("Superficie");
            modeloDatos.addColumn("Valor");
            modeloDatos.addColumn("Ubicacion");
            modeloDatos.addColumn("Año");
            modeloDatos.addColumn("Total");
            //imprimir registros
            while (rs.next()) {
                Object fila[] = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = rs.getObject(i + 1);
                }//termina for
                modeloDatos.addRow(fila);
            }//termina while

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error bd consulta" + ex);
        }
    }//GEN-LAST:event_btnActualizarPredioActionPerformed

